#include <stdlib.h>
#include "mlx.h"
#include "libft.h"
#include "mlxlib.h"

t_mlx		*ft_mlxdel(t_mlx *mlx)
{
	if (mlx->window != NULL)
		mlx_destroy_window(mlx->mlx, mlx->window);
	if (mlx->cam != NULL)
		ft_memdel((void **)&mlx->cam);
	if (mlx->mouse != NULL)
		ft_memdel((void **)&mlx->mouse);
	if (mlx->image != NULL)
		ft_del_image(mlx, mlx->image);
	ft_memdel((void **)&mlx);
	return (NULL);
}
